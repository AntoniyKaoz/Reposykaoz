package com.modelo;

public class Estudiante {
	
	private int idEstu;
	private String nombre;
	private int edad;
	
	public void setIdEstu(int idEstu){
		this.idEstu = idEstu;
	}
	
	public int getIdEstu() {
		return idEstu;
	}
	
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	public String getNombre() {
		return nombre;
	}
	
	public void setEdad(int edad) {
		this.edad = edad;
	}
	
	public int getEdad() {
		return edad;
	}
	
}
